num = int(input("Ingresar un número entre 1 - 7: "))

if num == 1:
    print("Lunes")
elif num == 2:
    print("Martes")
elif num == 3:
    print("Miercoles")
elif num == 4:
    print("Jueves")
elif num == 5:
    print("Viernes")
elif num == 6:
    print("Sabado")
elif num == 7:
    print("Domingo")
else:
    print("Número incorrecto. Elige del 1 - 7.")